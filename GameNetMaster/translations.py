def get_translation(key, language='en'):
    """
    Get a translation for a given key in the specified language
    
    Args:
        key (str): The translation key
        language (str): The language code ('en' or 'fa')
        
    Returns:
        str: The translated text, or the key itself if not found
    """
    if key in translations:
        return translations[key].get(language, translations[key].get('en', key))
    return key

